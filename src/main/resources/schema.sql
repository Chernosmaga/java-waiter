DROP TABLE IF EXISTS status CASCADE;
DROP TABLE IF EXISTS employee CASCADE;
DROP TABLE IF EXISTS orders CASCADE;
DROP TABLE IF EXISTS admins CASCADE;
DROP TABLE IF EXISTS order_status CASCADE;
DROP TABLE IF EXISTS employee_shift CASCADE;
DROP TABLE IF EXISTS employee_orders CASCADE;
DROP TABLE IF EXISTS dish CASCADE;
DROP TABLE IF EXISTS dish_status CASCADE;
DROP TABLE IF EXISTS tips_qr CASCADE;
DROP TABLE IF EXISTS dishes CASCADE;
DROP TABLE IF EXISTS comments CASCADE;

CREATE TABLE IF NOT EXISTS status (
status_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
status_name VARCHAR NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS employee (
employee_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
phone VARCHAR(11) NOT NULL UNIQUE,
first_name VARCHAR NOT NULL,
surname VARCHAR NOT NULL,
user_password VARCHAR NOT NULL,
is_active BOOLEAN NOT NULL
);

CREATE TABLE IF NOT EXISTS orders (
order_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
guests INTEGER NOT NULL,
creation_time DATE NOT NULL,
bill_time DATE,
total REAL NOT NULL
);

CREATE TABLE IF NOT EXISTS admins (
employee_id INTEGER REFERENCES employee (employee_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS order_status (
order_id INTEGER REFERENCES orders (order_id) ON DELETE CASCADE,
status_id INTEGER REFERENCES status (status_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS employee_shift (
employee_id INTEGER NOT NULL REFERENCES employee (employee_id),
shift_start_time DATE NOT NULL,
shift_end_time DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS employee_orders (
employee_id INTEGER NOT NULL REFERENCES employee (employee_id),
order_id INTEGER NOT NULL REFERENCES orders (order_id)
);

CREATE TABLE IF NOT EXISTS dish (
dish_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
title VARCHAR NOT NULL UNIQUE,
is_available BOOL NOT NULL,
quantity INTEGER,
time_limit BIGINT NOT NULL,
price REAL
);

CREATE TABLE IF NOT EXISTS dish_status (
dish_id INTEGER REFERENCES dish (dish_id) ON DELETE CASCADE,
status_id INTEGER REFERENCES status (status_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS tips_qr (
employee_id INTEGER NOT NULL REFERENCES employee (employee_id) ON DELETE CASCADE,
qr_code VARCHAR NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS dishes (
dish_id INTEGER NOT NULL REFERENCES dish (dish_id) ON DELETE CASCADE,
order_id INTEGER NOT NULL REFERENCES orders (order_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS comments (
order_id INTEGER REFERENCES orders (order_id) ON DELETE CASCADE,
dish_id INTEGER REFERENCES dish (dish_id) ON DELETE CASCADE,
comments VARCHAR
);